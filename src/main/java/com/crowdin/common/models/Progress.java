package com.crowdin.common.models;

public class Progress {
    private String languageId;
    private int phrasesCount;
    private int phrasesTranslatedCount;
    private int phrasesApprovedCount;
    private int phrasesTranslatedProgress;
    private int phrasesApprovedProgress;
    private int wordsCount;
    private int wordsTranslatedCount;
    private int wordsApprovedCount;
    private int wordsTranslatedProgress;
    private int wordsApprovedProgress;

    public String getLanguageId() {
        return languageId;
    }

    public void setLanguageId(String languageId) {
        this.languageId = languageId;
    }

    public int getPhrasesCount() {
        return phrasesCount;
    }

    public void setPhrasesCount(int phrasesCount) {
        this.phrasesCount = phrasesCount;
    }

    public int getPhrasesTranslatedCount() {
        return phrasesTranslatedCount;
    }

    public void setPhrasesTranslatedCount(int phrasesTranslatedCount) {
        this.phrasesTranslatedCount = phrasesTranslatedCount;
    }

    public int getPhrasesApprovedCount() {
        return phrasesApprovedCount;
    }

    public void setPhrasesApprovedCount(int phrasesApprovedCount) {
        this.phrasesApprovedCount = phrasesApprovedCount;
    }

    public int getPhrasesTranslatedProgress() {
        return phrasesTranslatedProgress;
    }

    public void setPhrasesTranslatedProgress(int phrasesTranslatedProgress) {
        this.phrasesTranslatedProgress = phrasesTranslatedProgress;
    }

    public int getPhrasesApprovedProgress() {
        return phrasesApprovedProgress;
    }

    public void setPhrasesApprovedProgress(int phrasesApprovedProgress) {
        this.phrasesApprovedProgress = phrasesApprovedProgress;
    }

    public int getWordsCount() {
        return wordsCount;
    }

    public void setWordsCount(int wordsCount) {
        this.wordsCount = wordsCount;
    }

    public int getWordsTranslatedCount() {
        return wordsTranslatedCount;
    }

    public void setWordsTranslatedCount(int wordsTranslatedCount) {
        this.wordsTranslatedCount = wordsTranslatedCount;
    }

    public int getWordsApprovedCount() {
        return wordsApprovedCount;
    }

    public void setWordsApprovedCount(int wordsApprovedCount) {
        this.wordsApprovedCount = wordsApprovedCount;
    }

    public int getWordsTranslatedProgress() {
        return wordsTranslatedProgress;
    }

    public void setWordsTranslatedProgress(int wordsTranslatedProgress) {
        this.wordsTranslatedProgress = wordsTranslatedProgress;
    }

    public int getWordsApprovedProgress() {
        return wordsApprovedProgress;
    }

    public void setWordsApprovedProgress(int wordsApprovedProgress) {
        this.wordsApprovedProgress = wordsApprovedProgress;
    }
}
